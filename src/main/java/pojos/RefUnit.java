package pojos;


public class RefUnit {
    private int refUnitId;
    private String refUnitName;
    private String refUnitCode;

    /**
     *
     */
    public RefUnit(){

    }

    /**
     * @return the refUnitId
     */
    public int getRefUnitId() {
        return refUnitId;
    }

    /**
     * @param refUnitId the refUnitId to set
     */
    public void setRefUnitId(int refUnitId) {
        this.refUnitId = refUnitId;
    }

    /**
     * @return the refUnitName
     */
    public String getRefUnitName() {
        return refUnitName;
    }

    /**
     * @param refUnitName the refUnitName to set
     */
    public void setRefUnitName(String refUnitName) {
        this.refUnitName = refUnitName;
    }

    /**
     * @return the refUnitCode
     */
    public String getRefUnitCode() {
        return refUnitCode;
    }

    /**
     * @param refUnitCode the refUnitCode to set
     */
    public void setRefUnitCode(String refUnitCode) {
        this.refUnitCode = refUnitCode;
    }

  }
